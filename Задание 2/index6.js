var a = Number(prompt("Введите число от 1 до 9"));

switch (a) {
    case 1:
        alert(a + " = I");
        break;
    case 2:
        alert(a + " = II");
        break;
    case 3:
        alert(a + " = III");
        break;
    case 4:
        alert(a + " = IV");
        break;
    case 5:
        alert(a + " = V");
        break;
    case 6:
        alert(a + " = VI");
        break;
    case 7:
        alert(a + " = VII");
        break;
    case 8:
        alert(a + " = VIII");
        break;
    case 9:
        alert(a + " = IX");
        break;
    default:
        alert("ERROR: Введите число от 1 до 9");
}
